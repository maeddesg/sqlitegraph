/// Lock manager for V2 resources
pub struct V2LockManager {
    /// Lock table: resource -> (lock_type, owners)
    lock_table: Arc<RwLock<HashMap<ResourceId, (LockType, HashSet<TransactionId>)>>>,

    /// Waiting queue for lock requests
    wait_queue: Arc<Mutex<VecDeque<LockRequest>>>,

    /// Lock wait timeout
    lock_timeout: Duration,

    /// Deadlock detector for wait-for graph management
    deadlock_detector: Arc<DeadlockDetector>,
}

impl V2LockManager {
    /// Create new lock manager
    pub fn new(deadlock_detector: Arc<DeadlockDetector>) -> Self {
        Self {
            lock_table: Arc::new(RwLock::new(HashMap::new())),
            wait_queue: Arc::new(Mutex::new(VecDeque::new())),
            lock_timeout: Duration::from_secs(30),
            deadlock_detector,
        }
    }

    /// Acquire lock for transaction
    pub fn acquire_lock(
        &self,
        tx_id: TransactionId,
        resource_id: ResourceId,
        lock_type: LockType,
    ) -> NativeResult<bool> {
        let mut lock_table = self.lock_table.write();
        let entry = lock_table
            .entry(resource_id)
            .or_insert((LockType::Shared, HashSet::new()));

        match lock_type {
            LockType::Shared => {
                // Shared locks can coexist
                entry.1.insert(tx_id);
                Ok(true)
            }
            LockType::Exclusive => {
                // Exclusive lock requires no other owners
                if entry.1.is_empty() || (entry.1.len() == 1 && entry.1.contains(&tx_id)) {
                    entry.0 = LockType::Exclusive;
                    entry.1.insert(tx_id);
                    Ok(true)
                } else {
                    // Lock held by others - add to wait-for graph
                    for &holder in &entry.1 {
                        if holder != tx_id {
                            self.deadlock_detector.add_wait_edge(tx_id, holder);
                        }
                    }
                    Ok(false)
                }
            }
            LockType::IntentionShared => {
                // Intention shared locks can coexist
                entry.1.insert(tx_id);
                Ok(true)
            }
            LockType::IntentionExclusive => {
                // Intention exclusive locks behave like shared for compatibility
                entry.1.insert(tx_id);
                Ok(true)
            }
        }
    }

    /// Release lock for transaction
    pub fn release_lock(&self, tx_id: TransactionId, resource_id: ResourceId) -> NativeResult<()> {
        let mut lock_table = self.lock_table.write();
        if let Some(entry) = lock_table.get_mut(&resource_id) {
            entry.1.remove(&tx_id);
            if entry.1.is_empty() {
                lock_table.remove(&resource_id);
            }
        }
        Ok(())
    }

    /// Add lock request to wait queue
    pub fn add_to_wait_queue(&self, request: LockRequest) -> NativeResult<()> {
        let mut wait_queue = self.wait_queue.lock();
        wait_queue.push_back(request);
        Ok(())
    }

    /// Process waiting queue
    pub fn process_wait_queue(&self) -> NativeResult<()> {
        let mut wait_queue = self.wait_queue.lock();
        let mut to_remove = Vec::new();

        for (i, request) in wait_queue.iter().enumerate() {
            if self.acquire_lock(
                request.transaction_id,
                request.resource_id,
                request.lock_type,
            )? {
                to_remove.push(i);
            }
        }

        // Remove granted requests from queue (in reverse order to maintain indices)
        for &i in to_remove.iter().rev() {
            wait_queue.remove(i);
        }

        Ok(())
    }

    /// Get the lock table for testing purposes
    #[cfg(test)]
    pub fn lock_table_for_test(
        &self,
    ) -> &Arc<RwLock<HashMap<ResourceId, (LockType, HashSet<TransactionId>)>>> {
        &self.lock_table
    }
}

/// Deadlock detector using wait-for graph
pub struct DeadlockDetector {
    /// Wait-for graph: transaction -> set of transactions it's waiting for
    wait_for_graph: Arc<RwLock<HashMap<TransactionId, HashSet<TransactionId>>>>,

    /// Resource-level wait graph: resource -> set of transactions waiting for this resource
    resource_wait_graph: Arc<RwLock<HashMap<ResourceId, HashSet<TransactionId>>>>,

    /// Reverse mapping: transaction -> set of resources it's waiting for
    tx_waiting_for: Arc<RwLock<HashMap<TransactionId, HashSet<ResourceId>>>>,

    /// Last detection timestamp
    last_detection: Arc<Mutex<Instant>>,

    /// Detection interval
    detection_interval: Duration,
}

impl DeadlockDetector {
    /// Create new deadlock detector
    pub fn new() -> Self {
        Self {
            wait_for_graph: Arc::new(RwLock::new(HashMap::new())),
            resource_wait_graph: Arc::new(RwLock::new(HashMap::new())),
            tx_waiting_for: Arc::new(RwLock::new(HashMap::new())),
            last_detection: Arc::new(Mutex::new(Instant::now())),
            detection_interval: Duration::from_millis(100),
        }
    }

    /// Check if acquiring this lock would cause deadlock
    pub fn would_cause_deadlock(
        &self,
        tx_id: TransactionId,
        resource_id: ResourceId,
    ) -> NativeResult<bool> {
        // Use enhanced resource-aware detection
        self.detect_resource_deadlock(tx_id, resource_id)
    }

    /// Detect deadlock using resource-level wait graph
    ///
    /// Checks both transaction-level cycles (Tx A waits for Tx B) and
    /// resource-level cycles (Tx A waits for Resource R1 held by Tx B,
    /// which waits for Resource R2 held by Tx A).
    pub fn detect_resource_deadlock(
        &self,
        tx_id: TransactionId,
        resource: ResourceId,
    ) -> NativeResult<bool> {
        // Check transaction-level cycle first
        if self.detect_cycle(tx_id)? {
            return Ok(true);
        }

        // Check resource-level cycle
        let resource_graph = self.resource_wait_graph.read();
        let tx_waiting = self.tx_waiting_for.read();

        // Build resource-aware wait-for graph
        let mut visited = HashSet::new();
        let mut recursion_stack = Vec::new();

        self.has_resource_cycle_util(
            tx_id,
            resource,
            &resource_graph,
            &tx_waiting,
            &mut visited,
            &mut recursion_stack,
        )
    }

    /// DFS utility for resource-level cycle detection
    ///
    /// Traverses the resource wait graph to detect cycles that involve
    /// both transactions and resources.
    fn has_resource_cycle_util(
        &self,
        tx_id: TransactionId,
        resource_waiting_on: ResourceId,
        resource_graph: &HashMap<ResourceId, HashSet<TransactionId>>,
        tx_waiting: &HashMap<TransactionId, HashSet<ResourceId>>,
        visited: &mut HashSet<TransactionId>,
        recursion_stack: &mut Vec<TransactionId>,
    ) -> NativeResult<bool> {
        visited.insert(tx_id);
        recursion_stack.push(tx_id);

        // Find transactions holding the resource we're waiting for
        if let Some(holders) = resource_graph.get(&resource_waiting_on) {
            for holder in holders {
                if recursion_stack.contains(holder) {
                    return Ok(true); // Cycle detected
                }

                if !visited.contains(holder) {
                    // Recursively check what holder is waiting for
                    if let Some(resources) = tx_waiting.get(holder) {
                        for res in resources {
                            if self.has_resource_cycle_util(
                                *holder,
                                *res,
                                resource_graph,
                                tx_waiting,
                                visited,
                                recursion_stack,
                            )? {
                                return Ok(true);
                            }
                        }
                    }
                }
            }
        }

        recursion_stack.pop();
        Ok(false)
    }

    /// Remove transaction from deadlock detector
    pub fn remove_transaction(&self, tx_id: TransactionId) {
        let mut wait_for_graph = self.wait_for_graph.write();
        wait_for_graph.remove(&tx_id);

        // Remove this transaction from all wait lists
        for waiting_set in wait_for_graph.values_mut() {
            waiting_set.remove(&tx_id);
        }

        // Clean up resource wait graphs
        let mut resource_graph = self.resource_wait_graph.write();
        for waiters in resource_graph.values_mut() {
            waiters.remove(&tx_id);
        }
        // Remove empty resource entries
        resource_graph.retain(|_, waiters| !waiters.is_empty());

        // Remove transaction's resource waits
        let mut tx_waiting = self.tx_waiting_for.write();
        tx_waiting.remove(&tx_id);
    }

    /// Add a wait edge: tx_waiter is waiting for tx_holder to release a lock
    pub fn add_wait_edge(&self, tx_waiter: TransactionId, tx_holder: TransactionId) {
        let mut wait_for_graph = self.wait_for_graph.write();
        wait_for_graph
            .entry(tx_waiter)
            .or_insert_with(HashSet::new)
            .insert(tx_holder);
    }

    /// Add a resource wait edge: transaction is waiting for a resource
    pub fn add_resource_wait(
        &self,
        tx_id: TransactionId,
        resource: ResourceId,
    ) -> NativeResult<()> {
        // Add to resource -> waiters mapping
        let mut resource_graph = self.resource_wait_graph.write();
        resource_graph
            .entry(resource)
            .or_insert_with(HashSet::new)
            .insert(tx_id);

        // Add to tx -> resources mapping
        let mut tx_waiting = self.tx_waiting_for.write();
        tx_waiting
            .entry(tx_id)
            .or_insert_with(HashSet::new)
            .insert(resource);

        Ok(())
    }

    /// Remove a resource wait edge
    pub fn remove_resource_wait(
        &self,
        tx_id: TransactionId,
        resource: ResourceId,
    ) -> NativeResult<()> {
        // Remove from resource -> waiters
        let mut resource_graph = self.resource_wait_graph.write();
        if let Some(waiters) = resource_graph.get_mut(&resource) {
            waiters.remove(&tx_id);
            if waiters.is_empty() {
                resource_graph.remove(&resource);
            }
        }

        // Remove from tx -> resources
        let mut tx_waiting = self.tx_waiting_for.write();
        if let Some(resources) = tx_waiting.get_mut(&tx_id) {
            resources.remove(&resource);
            if resources.is_empty() {
                tx_waiting.remove(&tx_id);
            }
        }

        Ok(())
    }

    /// Detect if a cycle exists starting from the given transaction
    pub fn detect_cycle(&self, start_tx: TransactionId) -> NativeResult<bool> {
        let wait_for_graph = self.wait_for_graph.read();
        let mut visited = HashSet::new();
        let mut recursion_stack = HashSet::new();

        Ok(self.has_cycle_util(
            start_tx,
            &mut visited,
            &mut recursion_stack,
            &wait_for_graph,
        ))
    }

    /// DFS utility for cycle detection
    fn has_cycle_util(
        &self,
        tx_id: TransactionId,
        visited: &mut HashSet<TransactionId>,
        recursion_stack: &mut HashSet<TransactionId>,
        wait_for_graph: &HashMap<TransactionId, HashSet<TransactionId>>,
    ) -> bool {
        if recursion_stack.contains(&tx_id) {
            return true;
        }

        if visited.contains(&tx_id) {
            return false;
        }

        visited.insert(tx_id);
        recursion_stack.insert(tx_id);

        if let Some(waiting_for) = wait_for_graph.get(&tx_id) {
            for &waiting_tx in waiting_for {
                if self.has_cycle_util(waiting_tx, visited, recursion_stack, wait_for_graph) {
                    return true;
                }
            }
        }

        recursion_stack.remove(&tx_id);
        false
    }

    /// Select the youngest transaction from a cycle as the deadlock victim
    ///
    /// Returns the transaction ID with the latest start_time (youngest).
    /// If multiple transactions have the same start_time, returns the
    /// transaction with the highest ID (most recently allocated).
    pub fn select_victim(
        &self,
        cycle: &[TransactionId],
        contexts: &HashMap<TransactionId, TransactionContext>,
    ) -> Option<TransactionId> {
        cycle
            .iter()
            .max_by_key(|&&tx_id| {
                contexts
                    .get(&tx_id)
                    .map(|c| (c.start_time, tx_id))
                    .unwrap_or((Instant::now(), tx_id))
            })
            .copied()
    }

    /// Detect and return the cycle path starting from the given transaction
    pub fn get_cycle(&self, start_tx: TransactionId) -> NativeResult<Vec<TransactionId>> {
        let wait_for_graph = self.wait_for_graph.read();
        let mut visited = HashSet::new();
        let mut path = Vec::new();
        let mut recursion_stack = HashSet::new();

        if self.find_cycle_path(
            start_tx,
            &mut visited,
            &mut recursion_stack,
            &mut path,
            &wait_for_graph,
        ) {
            Ok(path)
        } else {
            Ok(Vec::new())
        }
    }

    /// Helper to find the actual cycle path
    fn find_cycle_path(
        &self,
        tx_id: TransactionId,
        visited: &mut HashSet<TransactionId>,
        recursion_stack: &mut HashSet<TransactionId>,
        path: &mut Vec<TransactionId>,
        wait_for_graph: &HashMap<TransactionId, HashSet<TransactionId>>,
    ) -> bool {
        if recursion_stack.contains(&tx_id) {
            // Found the cycle - return path from this transaction
            if let Some(pos) = path.iter().position(|&x| x == tx_id) {
                path.truncate(pos + 1);
            }
            return true;
        }

        if visited.contains(&tx_id) {
            return false;
        }

        visited.insert(tx_id);
        recursion_stack.insert(tx_id);
        path.push(tx_id);

        if let Some(waiting_for) = wait_for_graph.get(&tx_id) {
            for &waiting_tx in waiting_for {
                if self.find_cycle_path(waiting_tx, visited, recursion_stack, path, wait_for_graph)
                {
                    return true;
                }
            }
        }

        recursion_stack.remove(&tx_id);
        path.pop();
        false
    }

    /// Periodic cleanup to prevent unbounded growth
    ///
    /// Removes entries for transactions that are no longer active.
    /// Should be called periodically (e.g., after every 1000 transactions).
    pub fn cleanup_stale_transactions(&self, active_ids: &HashSet<TransactionId>) -> usize {
        let mut wait_for_graph = self.wait_for_graph.write();
        let initial_size = wait_for_graph.len();

        // Remove entries for non-active transactions
        wait_for_graph.retain(|tx_id, _waiting_for| active_ids.contains(tx_id));

        // Also clean up references to non-active transactions
        for waiting_set in wait_for_graph.values_mut() {
            waiting_set.retain(|tx_id| active_ids.contains(tx_id));
        }

        let cleaned = initial_size - wait_for_graph.len();

        // Clean up resource wait graphs
        let mut resource_graph = self.resource_wait_graph.write();
        let mut tx_waiting = self.tx_waiting_for.write();

        // Remove non-active transactions from resource waiters
        for waiters in resource_graph.values_mut() {
            waiters.retain(|tx_id| active_ids.contains(tx_id));
        }
        // Remove resources with no waiters
        resource_graph.retain(|_, waiters| !waiters.is_empty());

        // Remove non-active transactions from tx_waiting_for
        tx_waiting.retain(|tx_id, _| active_ids.contains(tx_id));

        if cleaned > 0 {
            log::debug!("Cleaned {} stale deadlock detector entries", cleaned);
        }

        *self.last_detection.lock() = Instant::now();
        cleaned
    }

    /// Get size of wait-for graph (for monitoring)
    pub fn graph_size(&self) -> usize {
        self.wait_for_graph.read().len()
    }

    /// Check if cleanup is needed based on graph size
    pub fn needs_cleanup(&self, threshold: usize) -> bool {
        self.graph_size() > threshold
    }

    /// Get the wait-for graph for testing purposes
    #[cfg(test)]
    pub fn wait_for_graph_for_test(
        &self,
    ) -> &Arc<RwLock<HashMap<TransactionId, HashSet<TransactionId>>>> {
        &self.wait_for_graph
    }

    /// Get the resource wait graph for testing purposes
    #[cfg(test)]
    pub fn resource_wait_graph_for_test(
        &self,
    ) -> &Arc<RwLock<HashMap<ResourceId, HashSet<TransactionId>>>> {
        &self.resource_wait_graph
    }

    /// Get the tx_waiting_for graph for testing purposes
    #[cfg(test)]
    pub fn tx_waiting_for_for_test(
        &self,
    ) -> &Arc<RwLock<HashMap<TransactionId, HashSet<ResourceId>>>> {
        &self.tx_waiting_for
    }
}

/// Lock type validator with compatibility matrix
///
/// Implements standard multi-granularity locking rules for lock
/// type compatibility, upgrades, and downgrades.
pub struct LockTypeValidator;

impl LockTypeValidator {
    /// Check if lock upgrade is allowed
    ///
    /// Validates whether a transaction holding lock type `from` can
    /// upgrade to lock type `to`.
    pub fn can_upgrade(from: LockType, to: LockType) -> bool {
        match (from, to) {
            // Same lock type always allowed
            (a, b) if a == b => true,

            // Intention locks can upgrade to stronger
            (LockType::IntentionShared, LockType::Shared) => true,
            (LockType::IntentionShared, LockType::Exclusive) => true,
            (LockType::IntentionExclusive, LockType::Exclusive) => true,

            // Shared can upgrade to Exclusive
            (LockType::Shared, LockType::Exclusive) => true,

            // IS -> IX allowed (intention hierarchy)
            (LockType::IntentionShared, LockType::IntentionExclusive) => true,

            // Downgrades
            (LockType::Exclusive, LockType::Shared) => true,
            (LockType::Exclusive, LockType::IntentionShared) => true,
            (LockType::Exclusive, LockType::IntentionExclusive) => true,
            (LockType::IntentionExclusive, LockType::IntentionShared) => true,

            // Other combinations not allowed
            _ => false,
        }
    }

    /// Check if two lock types conflict
    ///
    /// Returns true if the two lock types cannot coexist on the same resource.
    pub fn has_conflict(lock1: LockType, lock2: LockType) -> bool {
        match (lock1, lock2) {
            // Exclusive conflicts with everything except IS
            (LockType::Exclusive, LockType::Exclusive) => true,
            (LockType::Exclusive, LockType::Shared) => true,
            (LockType::Exclusive, LockType::IntentionExclusive) => true,
            (LockType::Shared, LockType::Exclusive) => true,
            (LockType::Shared, LockType::IntentionExclusive) => true,
            (LockType::IntentionExclusive, LockType::Shared) => true,
            (LockType::IntentionExclusive, LockType::Exclusive) => true,

            // IS compatible with everything (no conflicts)
            (LockType::IntentionShared, _) => false,
            (_, LockType::IntentionShared) => false,

            // S and S compatible
            (LockType::Shared, LockType::Shared) => false,

            // IX and IX compatible
            (LockType::IntentionExclusive, LockType::IntentionExclusive) => false,
        }
    }
}

/// Transaction ID manager with wraparound protection
///
/// Uses PostgreSQL's pattern: stop accepting writes at 1M transactions
/// before u64::MAX to prevent wraparound issues.
pub struct TransactionIdManager {
    next_id: Arc<Mutex<u64>>,
    warn_threshold: u64,
    hard_limit: u64,
    last_wrap_check: Arc<Mutex<Instant>>,
}

impl TransactionIdManager {
    /// Safety margin from u64::MAX (PostgreSQL pattern: 1M transactions)
    pub const SAFETY_MARGIN: u64 = 1_000_000;

    /// Warning threshold (10M transactions before hard limit)
    pub const WRAP_WARNING_THRESHOLD: u64 = u64::MAX - Self::SAFETY_MARGIN - 10_000_000;

    pub fn new() -> Self {
        Self {
            next_id: Arc::new(Mutex::new(1)),
            warn_threshold: Self::WRAP_WARNING_THRESHOLD,
            hard_limit: u64::MAX - Self::SAFETY_MARGIN,
            last_wrap_check: Arc::new(Mutex::new(Instant::now())),
        }
    }

    /// Allocate next transaction ID with wraparound protection
    pub fn allocate(&self) -> NativeResult<TransactionId> {
        let mut next_id = self.next_id.lock();
        let id = *next_id;

        // Check for wraparound danger
        if id >= self.hard_limit {
            return Err(NativeBackendError::TransactionIdExhaustion {
                current_id: id,
                remaining: self.hard_limit.saturating_sub(id),
            });
        }

        // Log warning if approaching threshold
        if id >= self.warn_threshold {
            log::warn!(
                "Transaction ID approaching wraparound: {} ({} remaining)",
                id,
                self.hard_limit.saturating_sub(id)
            );
        }

        *next_id = id.wrapping_add(1);
        Ok(id)
    }

    /// Check if transaction ID cleanup is needed
    pub fn needs_wraparound_protection(&self) -> bool {
        let current = *self.next_id.lock();
        current >= self.warn_threshold
    }

    /// Get current transaction ID (for monitoring)
    pub fn current_id(&self) -> u64 {
        *self.next_id.lock()
    }

    /// Get remaining transaction IDs before hard limit
    pub fn remaining_ids(&self) -> u64 {
        let current = *self.next_id.lock();
        self.hard_limit.saturating_sub(current)
    }
}

/// Isolation level manager with lock type tracking
pub struct IsolationManager {
    /// Active transactions with their isolation requirements
    transaction_isolation: Arc<RwLock<HashMap<TransactionId, IsolationLevel>>>,

    /// Read validation timestamps for repeatable read
    read_timestamps: Arc<RwLock<HashMap<ResourceId, SystemTime>>>,

    /// Track locks held by each transaction: tx_id -> (resource_id, lock_type)
    transaction_locks: Arc<RwLock<HashMap<TransactionId, HashMap<ResourceId, LockType>>>>,
}

impl IsolationManager {
    /// Create new isolation manager
    pub fn new() -> Self {
        Self {
            transaction_isolation: Arc::new(RwLock::new(HashMap::new())),
            read_timestamps: Arc::new(RwLock::new(HashMap::new())),
            transaction_locks: Arc::new(RwLock::new(HashMap::new())),
        }
    }

    /// Register transaction with isolation level
    pub fn register_transaction(&self, tx_id: TransactionId, isolation_level: IsolationLevel) {
        let mut transaction_isolation = self.transaction_isolation.write();
        transaction_isolation.insert(tx_id, isolation_level);
    }

    /// Unregister transaction
    pub fn unregister_transaction(&self, tx_id: TransactionId) {
        let mut transaction_isolation = self.transaction_isolation.write();
        transaction_isolation.remove(&tx_id);

        // Clean up lock tracking
        let mut transaction_locks = self.transaction_locks.write();
        transaction_locks.remove(&tx_id);
    }

    /// Record that a transaction has acquired a lock on a resource
    pub fn record_lock(
        &self,
        tx_id: TransactionId,
        resource_id: ResourceId,
        lock_type: LockType,
    ) {
        let mut transaction_locks = self.transaction_locks.write();
        transaction_locks
            .entry(tx_id)
            .or_insert_with(HashMap::new)
            .insert(resource_id, lock_type);
    }

    /// Record that a transaction has released a lock on a resource
    pub fn record_lock_release(&self, tx_id: TransactionId, resource_id: ResourceId) {
        let mut transaction_locks = self.transaction_locks.write();
        if let Some(locks) = transaction_locks.get_mut(&tx_id) {
            locks.remove(&resource_id);
            if locks.is_empty() {
                transaction_locks.remove(&tx_id);
            }
        }
    }

    /// Get the current lock type a transaction holds on a resource
    pub fn get_current_lock(
        &self,
        tx_id: TransactionId,
        resource_id: ResourceId,
    ) -> Option<LockType> {
        let transaction_locks = self.transaction_locks.read();
        transaction_locks.get(&tx_id)?.get(&resource_id).copied()
    }

    /// Get all locks held on a resource by other transactions
    pub fn get_locks_on_resource(
        &self,
        resource_id: ResourceId,
        exclude_tx: TransactionId,
    ) -> Vec<(TransactionId, LockType)> {
        let transaction_locks = self.transaction_locks.read();
        let mut result = Vec::new();

        for (&tx_id, locks) in transaction_locks.iter() {
            if tx_id != exclude_tx {
                if let Some(&lock_type) = locks.get(&resource_id) {
                    result.push((tx_id, lock_type));
                }
            }
        }

        result
    }

    /// Validate access according to isolation level and lock compatibility
    pub fn validate_access(
        &self,
        tx_id: TransactionId,
        resource_id: ResourceId,
        lock_type: LockType,
    ) -> NativeResult<()> {
        // 1. Check if transaction already holds a lock on this resource
        let current_lock = self.get_current_lock(tx_id, resource_id);

        if let Some(existing_lock) = current_lock {
            // 2. Check if upgrade is allowed
            if !LockTypeValidator::can_upgrade(existing_lock, lock_type) {
                return Err(NativeBackendError::InvalidState {
                    context: format!(
                        "Cannot upgrade lock from {:?} to {:?} on resource {:?}",
                        existing_lock, lock_type, resource_id
                    ),
                    source: None,
                });
            }
        } else {
            // 3. Check for conflicts with other transactions
            let conflicting_locks = self.get_locks_on_resource(resource_id, tx_id);
            for (_other_tx, other_lock) in conflicting_locks {
                if LockTypeValidator::has_conflict(lock_type, other_lock) {
                    return Err(NativeBackendError::DeadlockDetected {
                        tx_id,
                        conflicting_resources: vec![match resource_id {
                            ResourceId::Node(id) => id,
                            ResourceId::Edge(id) => id,
                            ResourceId::Cluster(id) => id,
                            ResourceId::StringTable(id) => id as i64,
                            ResourceId::FreeSpace => -1,
                        }],
                    });
                }
            }
        }

        // Check isolation level specific validation
        let transaction_isolation = self.transaction_isolation.read();

        if let Some(&isolation_level) = transaction_isolation.get(&tx_id) {
            match isolation_level {
                IsolationLevel::ReadCommitted => {
                    // Read committed is always allowed
                    Ok(())
                }
                IsolationLevel::RepeatableRead => {
                    // Check if resource has been modified since first read
                    let read_timestamps = self.read_timestamps.read();
                    if let Some(&_first_read_time) = read_timestamps.get(&resource_id) {
                        // In a real implementation, we'd check if resource was modified
                        // since _first_read_time. For now, allow all reads.
                        Ok(())
                    } else {
                        // First read - record timestamp
                        drop(read_timestamps);
                        let mut read_timestamps = self.read_timestamps.write();
                        read_timestamps.insert(resource_id, SystemTime::now());
                        Ok(())
                    }
                }
                IsolationLevel::Serializable => {
                    // Serializable would require more complex validation
                    // For now, allow all accesses
                    Ok(())
                }
                IsolationLevel::Snapshot => {
                    // Snapshot isolation would track snapshot state
                    // For now, allow all accesses
                    Ok(())
                }
            }
        } else {
            Err(NativeBackendError::TransactionNotFound { tx_id })
        }
    }
}

