/// Transaction identifier with uniqueness guarantees
pub type TransactionId = u64;

/// Resource identifier for lock management
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum ResourceId {
    Node(NativeNodeId),
    Edge(NativeEdgeId),
    Cluster(i64),
    StringTable(u32),
    FreeSpace,
}

/// Transaction isolation levels
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum IsolationLevel {
    ReadCommitted = 1,
    RepeatableRead = 2,
    Serializable = 3,
    Snapshot = 4,
}

/// Transaction state machine
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum TransactionState {
    Active,
    Preparing,
    Prepared,
    Committing,
    Committed,
    Aborting,
    Aborted,
}

/// Lock type for resource management
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum LockType {
    Shared,
    Exclusive,
    IntentionShared,
    IntentionExclusive,
}

/// Lock request for transaction
#[derive(Debug, Clone)]
pub struct LockRequest {
    pub transaction_id: TransactionId,
    pub resource_id: ResourceId,
    pub lock_type: LockType,
    pub timestamp: Instant,
    pub blocking: bool,
}

/// Transaction context with full state tracking
#[derive(Debug, Clone)]
pub struct TransactionContext {
    /// Unique transaction identifier
    pub tx_id: TransactionId,

    /// Transaction start timestamp
    pub start_time: Instant,

    /// Transaction isolation level
    pub isolation_level: IsolationLevel,

    /// Set of locked resources
    pub locked_resources: HashSet<ResourceId>,

    /// WAL records generated by this transaction
    pub wal_records: Vec<V2WALRecord>,

    /// Transaction state
    pub state: TransactionState,

    /// Dependencies on other transactions
    pub dependencies: HashSet<TransactionId>,

    /// Savepoints for nested transactions
    pub savepoints: Vec<Savepoint>,

    /// Read set for repeatable read validation
    pub read_set: HashSet<ResourceId>,

    /// Write set for conflict detection
    pub write_set: HashSet<ResourceId>,
}

